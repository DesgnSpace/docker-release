func TestHAProxyRenderBackendBasic(t *testing.T) {
	state := &UpstreamState{
		Service: "app",
		Servers: []Server{
			{Addr: "172.18.0.5:80"},
			{Addr: "172.18.0.6:80"},
		},
	}

	got := renderHAProxyBackend(state)

	if !strings.Contains(got, "# Generated by docker-release for HAProxy") {
		t.Error("missing HAProxy header")
	}
	if !strings.Contains(got, "backend app_be") {
		t.Error("missing backend block")
	}
	if !strings.Contains(got, "balance roundrobin") {
		t.Error("missing default roundrobin balance")
	}
	if !strings.Contains(got, "server s1 172.18.0.5:80") {
		t.Error("missing server 1")
	}
	if !strings.Contains(got, "server s2 172.18.0.6:80") {
		t.Error("missing server 2")
	}
	if strings.Contains(got, "cookie _srr_") {
		t.Error("cookie directive should not appear without cookie affinity")
	}
}

