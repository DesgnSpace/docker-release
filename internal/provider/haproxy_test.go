func TestHAProxyGenerateConfigWritesFile(t *testing.T) {
	dir := t.TempDir()
	p := NewHAProxy(dir, nil, "", "")

	state := &UpstreamState{
		Service: "webapp",
		Servers: []Server{{Addr: "172.18.0.5:3000"}},
	}

	if err := p.GenerateConfig(state); err != nil {
		t.Fatalf("generate error: %v", err)
	}

	path := filepath.Join(dir, "webapp.cfg")
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read error: %v", err)
	}

	content := string(data)
	if !strings.Contains(content, "backend webapp_be") {
		t.Error("file missing backend block")
	}
	if !strings.Contains(content, "172.18.0.5:3000") {
		t.Error("file missing server")
	}
	if !strings.Contains(content, "# Generated by docker-release for HAProxy") {
		t.Error("file missing HAProxy header")
	}

	tmp := path + ".tmp"
	if _, err := os.Stat(tmp); !os.IsNotExist(err) {
		t.Error("temp file not cleaned up")
	}
}
