func TestCaddyRenderUpstreamBasic(t *testing.T) {
	state := &UpstreamState{
		Service: "app",
		Servers: []Server{
			{Addr: "172.18.0.5:80"},
			{Addr: "172.18.0.6:80"},
		},
	}

	got := renderCaddyUpstream(state, "")

	if !strings.Contains(got, "# Generated by docker-release for Caddy") {
		t.Error("missing Caddy header")
	}
	if !strings.Contains(got, "(app_upstream) {") {
		t.Error("missing named snippet block")
	}
	if !strings.Contains(got, "172.18.0.5:80") {
		t.Error("missing server 1")
	}
	if !strings.Contains(got, "172.18.0.6:80") {
		t.Error("missing server 2")
	}
	if !strings.Contains(got, "lb_policy round_robin") {
		t.Error("missing default round_robin policy")
	}
}

