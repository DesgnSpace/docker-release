func TestCaddyRenderUpstreamWithPath(t *testing.T) {
	state := &UpstreamState{
		Service: "linear_app",
		Servers: []Server{
			{Addr: "172.18.0.5:80"},
			{Addr: "172.18.0.6:80"},
		},
	}

	got := renderCaddyUpstream(state, "/linear")

	if !strings.Contains(got, "handle_path /linear*") {
		t.Error("missing handle_path block")
	}
	if strings.Contains(got, "(linear_app_upstream)") {
		t.Error("should not render named snippet when path is set")
	}
	if !strings.Contains(got, "172.18.0.5:80") {
		t.Error("missing server 1")
	}
}

func TestCaddyGenerateConfigWritesFile(t *testing.T) {
	dir := t.TempDir()
	p := NewCaddy(dir, nil, "", "")

	state := &UpstreamState{
		Service: "webapp",
		Servers: []Server{{Addr: "172.18.0.5:3000"}},
	}

	if err := p.GenerateConfig(state); err != nil {
		t.Fatalf("generate error: %v", err)
	}

	path := filepath.Join(dir, "webapp.caddy")
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read error: %v", err)
	}

	content := string(data)
	if !strings.Contains(content, "(webapp_upstream) {") {
		t.Error("file missing snippet block")
	}
	if !strings.Contains(content, "172.18.0.5:3000") {
		t.Error("file missing server")
	}
	if !strings.Contains(content, "# Generated by docker-release for Caddy") {
		t.Error("file missing Caddy header")
	}

	tmp := path + ".tmp"
	if _, err := os.Stat(tmp); !os.IsNotExist(err) {
		t.Error("temp file not cleaned up")
	}
}
