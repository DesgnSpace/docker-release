func TestAngieGenerateConfigWritesFile(t *testing.T) {
	dir := t.TempDir()
	p := NewAngie(dir, nil, "", "")

	state := &UpstreamState{
		Service: "webapp",
		Servers: []Server{
			{Addr: "172.18.0.5:3000"},
		},
	}

	if err := p.GenerateConfig(state); err != nil {
		t.Fatalf("generate error: %v", err)
	}

	path := filepath.Join(dir, "webapp.conf")
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read error: %v", err)
	}

	content := string(data)
	if !strings.Contains(content, "upstream webapp_upstream {") {
		t.Error("file missing upstream block")
	}
	if !strings.Contains(content, "server 172.18.0.5:3000;") {
		t.Error("file missing server")
	}
	if !strings.Contains(content, "# Generated by docker-release for Angie") {
		t.Error("file missing Angie header")
	}

	// No temp file left behind
	tmp := path + ".tmp"
	if _, err := os.Stat(tmp); !os.IsNotExist(err) {
		t.Error("temp file not cleaned up")
	}
}

func TestAngieRenderUpstreamStickyLearn(t *testing.T) {
	state := &UpstreamState{
		Service:         "app",
		Affinity:        "cookie",
		StickyLearnName: "sessionid",
		Servers:         []Server{{Addr: "172.18.0.5:80"}},
	}

	got := renderAngieUpstream(state)

	if !strings.Contains(got, "sticky learn") {
		t.Error("missing sticky learn directive")
	}
	if !strings.Contains(got, "create=$upstream_cookie_sessionid") {
		t.Error("missing create param")
	}
	if !strings.Contains(got, "lookup=$cookie_sessionid") {
		t.Error("missing lookup param")
	}
	if !strings.Contains(got, "zone=app_sessions:1m") {
		t.Error("missing zone")
	}
	if strings.Contains(got, "sticky cookie") {
		t.Error("should not use sticky cookie when learn name is set")
	}
}

